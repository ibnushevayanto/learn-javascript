// Variable Assignment

let result;
const userInput = 19;

result = userInput + 18;
console.log(`First Result Is ${result}`)
result = result - userInput;
console.log(`Second Result Is ${result} `)
result = result / userInput;
console.log(`Third Result Is ${result} `)
result = result * userInput;
console.log(`Fourth Result Is ${result} `)

alert(`The User Input Is ${userInput}`)
alert(`The Result Is ${result}`)